import ollama
from pydantic import BaseModel

class ClientIssue(BaseModel):
    title: str             # общая проблема
    sentiment: str        # тональность: positive, negative или neutral
    score: float           # оценка от 0 до 10
    key_points: list[str]  # ключевые моменты
    error_names: list[str]  #названия ошибок
    core_problem: str
    location_or_app: str
    
response = ollama.chat(
    model="bag_assistant_model:latest",
    messages=[{
        "role": "user",
        "content": """Проанализируй сообщение от клиента: 'нужно разобраться с определённой карточкой,, а именно в движении товаров. Проблемы две:Дата прописывается не полностью, не указывается последняя цифра года. Почему в движении товара указаны наименования каких-то других карточек. Сама карточка "Аминовитал", а в движении помимо неё: "Гаммарус", "Барс ФОРТЕ", "Ножницы для стрижки овец" - из-за чего возникает проблема в понимании слежении за остатками позиций. Движение начинается с сентября 2025 года.Что касаемо даты, которая прописывается не полностью.При переходе в раздел "Товары" - нажимаем на карточку товара - "Движение" -- открывается список движения товара с неполностью введенной датой (например, "13/02/202", а должна быть "13/02/2026"). Ситуация воспроизводится на СТ 5i, СТ 6 с EvotorPOS 8.20.1. У клиента также версия EvotorPOS 8.20.1'"""
    }],
    format=ClientIssue.model_json_schema()  # ← магия: JSON Schema из Pydantic
)
    # Парсим ответ в Pydantic-объект
issue = ClientIssue.model_validate_json(response["message"]["content"])

print(f"Проблема: {issue.title}")
print(f"Тональность: {issue.sentiment}")
print(f"Оценка: {issue.score}/10")
print("Ключевые моменты:")
for point in issue.key_points:
    print(f"  - {point}")
print(f"Суть проблемы: {issue.core_problem}")
print(f"Найденные ошибки/версии: {', '.join(issue.error_names)}")
print(f"Где воспроизводится: {issue.location_or_app}")

